package prueba;

public class Vehiculo {

    private String patente;
    private String modelo;

    public Vehiculo(String patente, String modelo)
    {

        this.patente = patente;
        this.modelo = modelo;

    }

    public String getPatente()
    {

        return patente;

    }

    public String getModelo()
    {

        return modelo;

    }

}
